    def getNeighbours(field, x, y):
        #log.append("x = " + str(x) + ", y = " + str(y) + ";")
        neighbours = 0
        for j in range(y-1, y+2):            
            for i in range(x-1, x+2):
                #log.append("i = " + str(i) + ", j = " + str(j) + ";")
                if not j < 0 and not i < 0 and not j > field.height - 1 and not i > field.width - 1 and not (j == y and i == x):
                    if field.cells[field.width*j + i] == 1:
                        neighbours += 1
        return neighbours
